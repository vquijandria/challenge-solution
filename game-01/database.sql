-- =============================================
-- DATABASE SCHEMA - Sistema de Suscripciones y Reportes Crediticios
-- =============================================

DROP TABLE IF EXISTS report_credit_cards;
DROP TABLE IF EXISTS report_other_debts;
DROP TABLE IF EXISTS report_loans;
DROP TABLE IF EXISTS subscription_reports;
DROP TABLE IF EXISTS subscriptions;

-- Tabla de suscripciones
CREATE TABLE subscriptions (
    id INT PRIMARY KEY AUTO_INCREMENT,
    full_name VARCHAR(255) NOT NULL,
    document VARCHAR(20) NOT NULL,
    email VARCHAR(255) NOT NULL,
    phone VARCHAR(20) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- Tabla de reportes de suscripción
CREATE TABLE subscription_reports (
    id INT PRIMARY KEY AUTO_INCREMENT,
    subscription_id INT NOT NULL,
    period VARCHAR(7) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (subscription_id) REFERENCES subscriptions(id) ON DELETE CASCADE
);

-- Tabla de préstamos
CREATE TABLE report_loans (
    id INT PRIMARY KEY AUTO_INCREMENT,
    subscription_report_id INT NOT NULL,
    bank VARCHAR(100) NOT NULL,
    status VARCHAR(3) NOT NULL,
    currency VARCHAR(3) NOT NULL DEFAULT 'PEN',
    amount DECIMAL(12,2) NOT NULL,
    expiration_days INT NOT NULL DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (subscription_report_id) REFERENCES subscription_reports(id) ON DELETE CASCADE
);

-- Tabla de otras deudas
CREATE TABLE report_other_debts (
    id INT PRIMARY KEY AUTO_INCREMENT,
    subscription_report_id INT NOT NULL,
    entity VARCHAR(100) NOT NULL,
    currency VARCHAR(3) NOT NULL DEFAULT 'PEN',
    amount DECIMAL(12,2) NOT NULL,
    expiration_days INT NOT NULL DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (subscription_report_id) REFERENCES subscription_reports(id) ON DELETE CASCADE
);

-- Tabla de tarjetas de crédito
CREATE TABLE report_credit_cards (
    id INT PRIMARY KEY AUTO_INCREMENT,
    subscription_report_id INT NOT NULL,
    bank VARCHAR(100) NOT NULL,
    currency VARCHAR(3) NOT NULL DEFAULT 'PEN',
    line DECIMAL(12,2) NOT NULL,
    used DECIMAL(12,2) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (subscription_report_id) REFERENCES subscription_reports(id) ON DELETE CASCADE
);

